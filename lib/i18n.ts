/* Dictionary: German string → [en, fr, nl]. Populated per public surface. */
const D: Record<string, [string, string, string]> = {}
export function addTranslations(entries: Record<string, [string, string, string]>) {
  Object.assign(D, entries)
}

const IDX: Record<Exclude<UiLang, 'de'>, 0 | 1 | 2> = { en: 0, fr: 1, nl: 2 }

/** Translate a German UI string; optional {placeholder} substitution. */
export function t(lang: UiLang | undefined | null, de: string, vars?: Record<string, string | number>): string {
  let out = de
  if (lang && lang !== 'de') {
    const row = D[de]
    if (row) out = row[IDX[lang]] || de
  }
  if (vars) for (const [k, v] of Object.entries(vars)) out = out.replaceAll(`{${k}}`, String(v))
  return out
}

